package j04_연산;

public class Operation01 {

	public static void main(String[] args) {

		int num = 10;
		
		num = num + 1;
		num++;
		++num;
		
		System.out.println(num++);		
		System.out.println(++num);
		System.out.println(num);
		
		System.out.println(--num);
		System.out.println(num--);
		System.out.println(num);
	}

}
